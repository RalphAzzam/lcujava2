     public Connection getConnection() {
        String JDBC_DRIVER = "com.mysql.jdbc.Driver";
        String URL = "jdbc:mysql://localhost:3306/gym_registration";
        String USERNAME = "root";
        String PASSWORD = "root";
        try {
            Class.forName(JDBC_DRIVER);
            con = DriverManager.getConnection(URL, USERNAME, PASSWORD);
            
        } catch (ClassNotFoundException | SQLException ex) {
            System.err.println(ex.getMessage());
        }
        return con;
    }
        public void refreshData(){
        entityManager0.getTransaction().begin();
        java.util.Collection data 
                = gymRegistrationClientsQuery.getResultList();
        for(Object entity: data){
            entityManager0.refresh(entity);
        }
        gymRegistrationClientsList.clear();
        gymRegistrationClientsList.addAll(data);
        bindingGroup.unbind();
        bindingGroup.bind();
        entityManager0.getTransaction().commit();

        }

